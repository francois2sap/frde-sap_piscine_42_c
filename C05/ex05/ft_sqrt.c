int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (0 >= nb || nb > 2147395600)
		return (0);
	while ((i * i) < nb)
		i++;
	if ((i * i) == nb)
		return (i);
	return (0);
}
